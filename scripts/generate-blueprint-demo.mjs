function pageContent(page, ctx) {
  const c = ctx

  if (c.isSmallRig) return smallRigPageContent(page, c)
  if (c.schemeType === 'brand_building_case') return brandBuildingPageContent(page, c)
  if (c.isStrategySoftware) return strategySoftwarePositioningPageContent(page, c)
  return genericPositioningPageContent(page, c)
}

function buildSlide(page, chunk, ctx, clientSlug) {
  const [actionTitle, corePoints] = pageContent(page, ctx)
  const pageIntent = pageText(page, 'page_intent', ctx)
  const pageSubtitle = pageText(page, 'page_subtitle', ctx)
  const dataSourceHint = pageText(page, 'data_source_hint', ctx)
  return {
    page_no: page.page_no,
    layout: page.recommended_layout,
    action_title: actionTitle,
    core_points: corePoints,
    data_refs: [
      {
        value: dataSourceHint || '客户输入资料',
        source: sourceFor(page, clientSlug, ctx),
        type: 'client_input',
      },
    ],
    models_used: [modelFor(page, chunk)],
    page_intent: pageIntent,
    page_subtitle: pageSubtitle,
    render_hints: {
      kpi_hero: page.page_no === 41 ? '品牌策划方案 Agent' : undefined,
      accent_color: page.page_no % 2 === 0 ? 'ink' : 'accent',
    },
  }
}

function chunkTakeawayFor(chunk, ctx) {
  const chunkTitle = deckText(ctx, chunk.chunk_title)
  if (ctx.isSmallRig) {
    return `${chunkTitle} 的结论是:${ctx.name} 必须严格回到 page-124 的“全球影像场景产品生态开创者”品牌屋字段`
  }

  if (ctx.isStrategySoftware) {
    return `${chunkTitle} 的结论是:${ctx.name} 必须被证明为“${ctx.categoryFrame}”,而不是通用 AI PPT 工具`
  }

  return `${chunkTitle} 的结论是:${ctx.name} 必须被证明为“${ctx.categoryFrame}”,并把人群、场景、利益和 RTB 收束成同一条定位判断`
}

function evidenceInsightFor(ctx) {
  if (ctx.isSmallRig) {
    return `${ctx.name} 的定位证据必须来自 page-124 的品牌定位、人群、利益、RTB 和产品系列字段`
  }

  if (ctx.isPptAgent) {
    return `${ctx.name} 的定位证据必须同时来自用户任务、竞品空位和 Seven 私有资产`
  }

  return `${ctx.name} 的定位证据必须同时来自目标人群、竞品空位和自身产品/服务配称`
}

function buildChunkOutput(chunk, ctx, strategicQuestionText, clientSlug) {
  const slides = chunk.pages.map(page => buildSlide(page, chunk, ctx, clientSlug))
  const chunkTitle = deckText(ctx, chunk.chunk_title)
  const mustAppearKeywords = (chunk.self_check?.must_appear_keywords || []).map(keyword => deckText(ctx, keyword))
  const keywordInsight = mustAppearKeywords.length
    ? `本段必须显性回应: ${mustAppearKeywords.join(' / ')}`
    : `本段围绕 ${chunkTitle} 推进整案判断`
  const insights = [
    `洞察问题:${deckText(ctx, chunk.chunk_insight_question)}`,
    keywordInsight,
    evidenceInsightFor(ctx),
  ].slice(0, Math.max(1, chunk.expected_insights_count || 1))

  while (insights.length < (chunk.expected_insights_count || 1)) {
    insights.push(`${chunkTitle} 需要回扣根问题而不是扩写通用模型`)
  }

  return {
    agent_id: chunk.driving_sub_agent,
    blueprint_chunk_id: chunk.chunk_id,
    chunk_takeaway: chunkTakeawayFor(chunk, ctx),
    chunk_insights: insights,
    thinking_log: [
      'Step 1: 读取 inputs/<slug>/strategic-question.md 的根问题',
      `Step 2: 对照 blueprint chunk_insight_question: ${deckText(ctx, chunk.chunk_insight_question)}`,
      'Step 3: 用客户 summary.md / form.json 中的事实填充每页,不足处只写假设',
    ],
    strategic_question_excerpt: strategicQuestionText.match(/## 根问题\s+([\s\S]*?)(?:\n## |\n$)/)?.[1]?.trim() || '',
    client_profile: {
      name: ctx.name,
      render_style: ctx.renderStyle,
    },
    slides,
    metadata: {
      blueprint_chunk_id: chunk.chunk_id,
      chunk_intent_acknowledged: deckText(ctx, chunk.chunk_intent),
      self_check_passed: true,
      self_check_notes: [
        'slides.length === chunk.pages.length',
        'models_used is subset of allowed_concepts',
        'data_refs sources point to local client input',
      ],
    },
  }
}

async function writeChunkOutputs(clientSlug, schemeType, ctx, options = {}) {
  const { blueprint, blueprintPath } = await loadBlueprintForScheme(schemeType)
  const chunks = flattenBlueprintChunks(blueprint, blueprintPath)
  const strategicQuestionPath = repoPath('inputs', clientSlug, 'strategic-question.md')
  const strategicQuestionText = await readText(strategicQuestionPath)
  const chunkDir = repoPath('outputs', clientSlug, '_chunks')
  await fs.mkdir(chunkDir, { recursive: true })

  const insights = []
  const reviews = []

  for (const chunk of chunks) {
    const checkChunk = adaptBlueprintChunkForContext(chunk, ctx)
    const outPath = path.join(chunkDir, `${chunk.chunk_id}.json`)
    if (!options.force) {
      try {
        await fs.access(outPath)
        continue
      } catch (error) {
        if (error.code !== 'ENOENT') throw error
      }
    }

    const output = buildChunkOutput(chunk, ctx, strategicQuestionText, clientSlug)
    const bpResult = blueprintCheck(output, checkChunk)
    if (!bpResult.passed) {
      throw new Error(`Blueprint check failed for ${chunk.chunk_id}: ${bpResult.errors.join('; ')}`)
    }

    const review = {
      chunk_id: chunk.chunk_id,
      passed: true,
      verdict: 'NOT_RUN',
      key_weakness: 'Historical deterministic demo generator does not run LLM consulting review.',
    }

    await fs.writeFile(outPath, JSON.stringify(output, null, 2))
    insights.push({
      chunk_id: chunk.chunk_id,
      chunk_title: chunk.chunk_title,
      chunk_takeaway: output.chunk_takeaway,
      chunk_insights: output.chunk_insights,
      feeds_into: chunk.feeds_into || [],
    })
    reviews.push(review)
  }

  const reviewSummary = summarizeSuiteReview(reviews)
  await fs.writeFile(repoPath('outputs', clientSlug, '_insights.json'), JSON.stringify({
    client_slug: clientSlug,
    scheme_type: schemeType,
    chunks: insights,
  }, null, 2))
  await fs.writeFile(repoPath('outputs', clientSlug, '_review.json'), JSON.stringify({
    client_slug: clientSlug,
    scheme_type: schemeType,
    summary: reviewSummary,
    reviews,
  }, null, 2))

  return { chunks, insights, reviews, reviewSummary }
}

export async function generateBlueprintDemo(clientSlug, schemeType, options = {}) {
  const outputSlug = options.outputSlug || `${clientSlug}-blueprint`
  const inputDir = repoPath('inputs', clientSlug)

  if (options.force) {
    await fs.rm(repoPath('outputs', clientSlug), { recursive: true, force: true })
    await fs.rm(repoPath('outputs', outputSlug), { recursive: true, force: true })
  }

  const form = await readJson(path.join(inputDir, 'form.json'))
  const summary = await readText(path.join(inputDir, 'summary.md'))
  const ctx = buildContext(form, summary, schemeType)

  await ensureStrategicQuestion(clientSlug, schemeType, { force: options.force })
  await runBlueprintSuite(clientSlug, schemeType, {
    skipExisting: !options.force,
    failFast: true,
    forceStrategicQuestion: false,
  })
  const generated = await writeChunkOutputs(clientSlug, schemeType, ctx, { force: options.force })
  const assembled = await assembleByBlueprint(clientSlug, schemeType, { outputSlug })
  const htmlPath = repoPath('outputs', outputSlug, 'index.html')
  await renderDeck(assembled.outPath, htmlPath, { style: ctx.renderStyle })

  return {
    clientSlug,
    outputSlug,
    totalPages: assembled.totalPages,
    rawPath: assembled.outPath,
    htmlPath,
    review: generated.reviewSummary,
  }
}

function parseArgs(args) {
  const positional = []
  const flags = { scheme: 'brand_positioning_case', outputSlug: null, force: false }

  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index]
    if (arg === '--scheme') {
      flags.scheme = args[index + 1]
      index += 1
      continue
    }
    if (arg.startsWith('--scheme=')) {
      flags.scheme = arg.split('=').slice(1).join('=')
      continue
    }
    if (arg === '--output-slug') {
      flags.outputSlug = args[index + 1]
      index += 1
      continue
    }
    if (arg.startsWith('--output-slug=')) {
      flags.outputSlug = arg.split('=').slice(1).join('=')
      continue
    }
    if (arg === '--force') {
      flags.force = true
      continue
    }
    if (arg.startsWith('--')) continue
    positional.push(arg)
  }

  return { clientSlug: positional[0], ...flags }
}

async function cliMain() {
  const { clientSlug, scheme, outputSlug, force } = parseArgs(process.argv.slice(2))
  if (!clientSlug) {
    console.error('Usage: node scripts/generate-blueprint-demo.mjs <client_slug> [--scheme brand_positioning_case|brand_building_case] [--output-slug <slug>] [--force]')
    process.exit(1)
  }

  const result = await generateBlueprintDemo(clientSlug, scheme, { outputSlug, force })
  console.log(`✅ Blueprint demo generated: ${result.totalPages} pages`)
  console.log(`Raw: ${path.relative(REPO_ROOT, result.rawPath)}`)
  console.log(`HTML: ${path.relative(REPO_ROOT, result.htmlPath)}`)
  console.log(`Review: ${JSON.stringify(result.review)}`)
}

if (process.argv[1] && fileURLToPath(import.meta.url) === path.resolve(process.argv[1])) {
  cliMain().catch(error => {
    console.error(error)
    process.exit(1)
  })
}
