function points(...items) {
  return items.filter(Boolean)
}

function softwareDeckText(text) {
  return String(text || '')
    .replace(/电商\/包装场景/g, '官网/产品界面场景')
    .replace(/包装设计：包装结构、开箱体验和品牌识别原则/g, '产品界面设计：关键流程、首次体验和品牌识别原则')
    .replace(/产品辅料：快递盒、礼品袋、手册卡片等触点物料/g, '产品辅助触点：引导页、帮助文档、模板卡片等触点物料')
    .replace(/设计汇总：品牌视觉、包装、KV 和关键物料回顾/g, '设计汇总：品牌视觉、产品界面、KV 和关键物料回顾')
    .replace(/包装设计 \+ 私域触点/g, '产品界面 + 私域触点')
    .replace(/包装/g, '产品界面')
    .replace(/快递盒/g, '引导页')
    .replace(/礼品袋/g, '帮助文档')
    .replace(/手册卡片/g, '模板卡片')
}

function pageText(page, key, ctx) {
  const value = page[key] || ''
  return ctx?.isStrategySoftware ? softwareDeckText(value) : String(value)
}

function deckText(ctx, text) {
  return ctx?.isStrategySoftware ? softwareDeckText(text) : String(text || '')
}

function adaptBlueprintChunkForContext(chunk, ctx) {
  if (!ctx?.isStrategySoftware) return chunk
  return {
    ...chunk,
    chunk_title: deckText(ctx, chunk.chunk_title),
    chunk_intent: deckText(ctx, chunk.chunk_intent),
    chunk_insight_question: deckText(ctx, chunk.chunk_insight_question),
    self_check: {
      ...(chunk.self_check || {}),
      must_appear_keywords: (chunk.self_check?.must_appear_keywords || []).map(keyword => deckText(ctx, keyword)),
    },
    pages: (chunk.pages || []).map(page => ({
      ...page,
      page_intent: pageText(page, 'page_intent', ctx),
      page_subtitle: pageText(page, 'page_subtitle', ctx),
      data_source_hint: pageText(page, 'data_source_hint', ctx),
    })),
  }
}

function pageFocus(page, ctx) {
  return (pageText(page, 'page_intent', ctx) || pageText(page, 'page_subtitle', ctx))
    .replace(/[「」]/g, '')
    .replace(/\s+/g, ' ')
    .trim()
}

